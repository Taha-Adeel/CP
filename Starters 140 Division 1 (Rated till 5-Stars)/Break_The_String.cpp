vi z_function(string s) {
	int n = s.size();
	vi z(n, 0);
	for(int i = 1, l = 0, r = 0; i < n; ++i){
		if(i <= r)
			z[i] = min(r - i + 1, z[i - l]);
		while(i + z[i] < n && s[z[i]] == s[i + z[i]])
			++z[i];
		if(i + z[i] - 1 > r)
			l = i, r = i + z[i] - 1;
	}
	return z;
}

void solve() {
    string s; cin >> s;

    int n = s.size();
    if (n % 2) {
        cout << 0;
        return;
    }

    vi z1 = z_function(s);
    reverse(all(s));
    vi z2 = z_function(s);

    int ans = (z2[n / 2] >= n / 2);
    for (int i = 0; i < n / 2; i++) {
        if (z1[i + 1] < i + 1) continue;
        if (z2[n / 2 - i - 1] >= n / 2 - i - 1) ans++;
    }

    cout << ans;
}

