void solve(){
	int n;
	cin >> n;
	V<string> a(n), b(n), c(n);
	cin >> a >> b >> c;

	auto a_freq = a.freqs(), b_freq = b.freqs(), c_freq = c.freqs();

	int a_score = 0, b_score = 0, c_score = 0;
	FOR(i, n){
		a_score += max(3 - 2*(b_freq[a[i]] + c_freq[a[i]]), 0);
		b_score += max(3 - 2*(a_freq[b[i]] + c_freq[b[i]]), 0);
		c_score += max(3 - 2*(b_freq[c[i]] + a_freq[c[i]]), 0);
	}

	cout << a_score << ' ' << b_score << ' ' << c_score;
}

