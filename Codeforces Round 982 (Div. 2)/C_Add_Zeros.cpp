/*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*/

void solve() {
    int n; cin >> n;
    vll a(n); cin >> a;

    for (int i = 0; i < n; i++) {
        a[i] += i - n;
    }

    auto indices = a.indices();
    vll dist(n, -1);
    queue<int> q;
    for (auto i: indices[0]) {
        dist[i] = i;
        q.push(i);
    }
    while(!q.empty()) {
        int i = q.front(); q.pop();
        for (auto j: indices[a[i] + i]) {
            if (dist[j] == -1) {
                dist[j] = dist[i] + j;
                q.push(j);
            }
        }
    }

    cout << n + max(0LL, *max_element(all(dist)));
}

int main() {
    FAST;
    int T; cin >> T;
    FOR(t, T) {
        solve();
        cout << nl;
    }
    
    return 0;
}