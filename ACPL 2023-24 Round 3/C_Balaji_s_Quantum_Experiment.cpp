int query(int l, int r) {
    cout << "? " << l << ' ' << r << endl;
    int ans; cin >> ans;
    return ans;
}

void solve(){
    int n; cin >> n;

    vi ans(n);
    int x12 = query(1, 2);
    int x13 = query(1, 3);
    int x23 = query(2, 3);

    ans[1] = x12 + x23 - x13;
    ans[0] = x12 - ans[1];
    ans[2] = x23 - ans[1];

    for(int i = 3; i < n; ++i) {
        ans[i] = query(i, i+1) - ans[i-1];
    }

    cout << "! " << ans;
}

