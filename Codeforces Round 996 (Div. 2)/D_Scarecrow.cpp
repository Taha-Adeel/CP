void solve() {
    int n, k, l; cin >> n >> k >> l;
    vi a(n); cin >> a;

    l *= 2; k *= 2;
    for (auto& i: a) {
        i *= 2;
    }

    auto closest = [](int x, int d, int t) {
        if (d - t <= x and x <= d + t) {
            return x;
        } else if (d - t > x) {
            return d - t;
        } else {
            return d + t;
        }
    };

    int t = a[0], x = k, i = 1;
    while (x < l and i < n) {
        int closest_d = closest(x, a[i], t);
        if (closest_d <= x) {
            x = max(closest_d + k, x);
            i++;
        } else {
            int dist = closest_d - x;
            t += dist / 2;
            x += dist / 2;
        }
    }
    t += max(l - x, 0);

    cout << t;
}

int main() {
    FAST;
    int T; cin >> T;
    FOR(t, T) {
        solve();
        cout << nl;
    }
    
    return 0;
}