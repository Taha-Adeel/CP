#define FOR(i, n)     for(int i = 0; i < (int)n; ++i)
#define FOR1(i, n)    for(int i = 1; i <= (int)n; ++i)
#define ROF(i, n)     for(int i = (int)n-1; i >= 0; --i)
#define all(v)        v.begin(), v.end()
#define pb push_back

constexpr char nl = '\n';
constexpr int MOD = 1000000007;
using ll  = long long int;
using pii = pair<int, int>;
using pll = pair<ll, ll>;
using vi  = V<int>;
using vll = V<ll>;

/*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*/

void solve() {
    int n; cin >> n;
    vll a(n), b(n); cin >> a >> b;

    if (a.sum() != b.sum()) {
        cout << "NO";
        return;
    }

    vll ps_a = a.prefix_sums(), ps_b = b.prefix_sums();
    vll ps_a_even, ps_a_odd, ps_b_even, ps_b_odd;
    FOR(i, n) {
        if (i % 2) {
            ps_a_odd.pb(ps_a[i]);
            ps_b_odd.pb(ps_b[i]);
        } else {
            ps_a_even.pb(ps_a[i]);
            ps_b_even.pb(ps_b[i]);
        }
    }
    ps_a_even.sort();
    ps_a_odd.sort();
    ps_b_even.sort();
    ps_b_odd.sort();

    if (ps_a_even == ps_b_even && ps_a_odd == ps_b_odd) {
        cout << "YES";
    } else {
        cout << "NO";
    }
}

int main() {
    FAST;
    int T; cin >> T;
    FOR(t, T) {
        solve();
        cout << nl;
    }
    
    return 0;
}