void solve() {
    string a, b, c; cin >> a >> b >> c;

    int n = a.size(), m = b.size();
    V<vi> dp(n + 1, vi(m + 1, INT_MAX)); // dp[i][j] = min ans if using first i chars of a and first j chars of b
    dp[0][0] = 0;
    for (int i = 0; i <= n; i++) {
        for (int j = 0; j <= m; j++) {
            if (i) dp[i][j] = min(dp[i][j], dp[i - 1][j] + (a[i - 1] != c[i + j - 1]));
            if (j) dp[i][j] = min(dp[i][j], dp[i][j - 1] + (b[j - 1] != c[i + j - 1]));
        }
    }

    cout << dp[n][m];
}

int main() {
    FAST;
    int T; cin >> T;
    FOR(t, T) {
        solve();
        cout << nl;
    }
    
    return 0;
}