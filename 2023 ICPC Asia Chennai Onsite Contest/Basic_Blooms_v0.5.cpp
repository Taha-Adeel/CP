int from_base_x(string num) {
    int base = 90;
    int res = 0;
    for (int i = 0; i < num.size(); ++i) {
        res *= base;
        int digit = -num[i];
        if(digit >= '?') digit--;
        if(digit >= '\\') digit--;
        res += digit - 35;
    }
    return res;
}

