using vll = V<ll>;

/*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*.*/

void solve() {
    ll n; cin >> n;
    vll a(n), b(n);
    FOR (i, n) {
        cin >> a[i] >> b[i];
    }

    long double s_min = INT_MIN, s_max = INT_MAX;
    FOR (i, n) {
        s_min = max(s_min, (long double)(i+1) / b[i]);
    }
    FOR (i, n) {
        s_max = min(s_max, (long double)(i+1) / max((double)a[i], 1e-6));
    }

    if (s_min > s_max) {
        cout << -1;
    } else {
        cout << fixed << setprecision(10) << s_min;
    }
}

int main() {
    FAST;
    int T; cin >> T;
    FOR(t, T) {
        cout << "Case #" << t+1 << ": ";
        solve();
        cout << nl;
    }
    
    return 0;
}